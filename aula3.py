'''
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica/Forte
str -> string -> texto
String são textos que estão entre aspas simples ou duplas
'''

#Aspas simples
print('Thiago')

#Aspas duplas
print("Thiago")

#Escape
print('Thiago \"Copetti\"')

# r
print(r"Thiago \"Copetti\"")

#Inverter aspas para não precisar usar escape
print("'Thiago Copetti'")