# if / elif / else
# se / se não se / se não

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = False

if condicao1:
    print('Este é o código da condicao 1')
elif condicao2:
    print('Este é o código da condicao 2')
elif condicao3:
    print('Este é o código da condicao 3')
elif condicao4:
    print('Este é o código da condicao 4')
else:
    print('Nenhuma condicao foi satisfeita')