#Tipo int e float
# int -> numero inteiro
#O tipo int representa qualquer número inteiro, positivo ou negativo, sem casas decimais.
# positivo ou negativo. int sem sinal e´considerado positivo

print(11)
print(-11)
print(0)

#float -> numero com ponto flutuante
#o tipo float representa qualquer numero
#positivo ou negativo com ponto flutuante
#float sem sinal e´considerado positivo
print(1.1, 10.11)
print(0.0, -1.5)

#A função type mostra o tipo que o python inferiu ao valor
print(type('Thiago'))
print(type(-1))
print(type(1.1))
